require "test_helper"

class OrderMailerTest < ActionMailer::TestCase
  test "new" do
    mail = OrderMailer.new
    assert_equal "New", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

  test "paid" do
    mail = OrderMailer.paid
    assert_equal "Paid", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

  test "fulfilled" do
    mail = OrderMailer.fulfilled
    assert_equal "Fulfilled", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

  test "delivered" do
    mail = OrderMailer.delivered
    assert_equal "Delivered", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

end
